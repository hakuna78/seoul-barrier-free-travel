def fetch(area_cd, api_key):
    url = f"http://openapi.seoul.go.kr:8088/{api_key}/json/citydata_ppltn/1/1/{area_cd}"
    try:
        res = requests.get(url, timeout=10)
        res.raise_for_status()
        data = res.json()
        for v in data.values():
            if isinstance(v, list) and v:
                return v[0], None
        return None, "빈 응답"
    except Exception as e:
        return None, str(e)

